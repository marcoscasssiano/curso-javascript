//Operadores de Igualdade

//igualde estrita: ===
console.log(1 === 1); //true por que são completamentes iguais
console.log("1" === 1); //false, por mais que pareça ser a mesma coisa um é um tipo numero e o outro é do tipo texto(string)

//igualdade solta
console.log(1 == 1); //true nada de novo até aqui, são exatamentes iguais.
