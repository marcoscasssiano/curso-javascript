// Operadores aritiméticos (ou operadores matematicos)

let valor = 10;

/*
soma: +
subtração: -
multiplicação: *
divisão: /
exponencial ** (um numero multiplicado por ele mesmo(ou "ao quadrdado", ou como preferir(kk)))
*/

console.log(valor + valor);
console.log(valor - valor);
console.log(valor * valor);
console.log(valor / valor);
console.log(valor ** valor);
